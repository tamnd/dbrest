// rowFilterDesc resolves a column's rowFilter parameter through the shared
// parameters map, the way a client follows the $ref an operation carries.
func rowFilterDesc(t *testing.T, doc map[string]any, table, col string) string {
	t.Helper()
	params, ok := doc["parameters"].(map[string]any)
	if !ok {
		t.Fatal("document has no parameters map")
	}
	p, ok := params["rowFilter."+table+"."+col].(map[string]any)
	if !ok {
		t.Fatalf("no rowFilter.%s.%s parameter found", table, col)
	}
	if p["name"] != col || p["in"] != "query" || p["type"] != "string" {
		t.Errorf("rowFilter.%s.%s = %v", table, col, p)
	}
	if req, present := p["required"]; !present || req != false {
		t.Errorf("rowFilter.%s.%s required = %v, want explicit false", table, col, req)
	}
	desc, _ := p["description"].(string)
	return desc
}

// TestOperatorAdvertisingHonorsCapabilities is the heart of the contract: a
// column parameter advertises match/imatch and the fts family on SQLite (regex
// and FTS5 present) but never the array/range operators (Unsupported).
func TestOperatorAdvertisingHonorsCapabilities(t *testing.T) {
	doc := decode(t, filmsModel(), nil, sqliteCaps(), Options{})
	colDesc := rowFilterDesc(t, doc, "films", "title")
	if colDesc == "" {
		t.Fatal("title rowFilter parameter has no description")
	}
	for _, want := range []string{"eq", "match", "imatch", "fts", "plfts", "phfts", "wfts"} {
		if !containsToken(colDesc, want) {
			t.Errorf("operator %q should be advertised; desc = %q", want, colDesc)
		}
	}
	for _, gone := range []string{"cs", "cd", "ov", "sl", "sr", "adj"} {
		if containsToken(colDesc, gone) {
			t.Errorf("operator %q should be omitted on SQLite; desc = %q", gone, colDesc)
		}
	}
}

// TestRegexOmittedWhenUnsupported drops match/imatch when the backend grades
