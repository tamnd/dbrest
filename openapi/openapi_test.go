// TestDocumentFraming pins the v14 framing: externalDocs pointing at the
// PostgREST reference, the vendor media types in the top-level consumes and
// produces, and the "/" entry describing the root itself.
func TestDocumentFraming(t *testing.T) {
	doc := decode(t, filmsModel(), nil, sqliteCaps(), Options{})

	ed, ok := doc["externalDocs"].(map[string]any)
	if !ok {
		t.Fatal("missing externalDocs")
	}
	if ed["description"] != "PostgREST Documentation" {
		t.Errorf("externalDocs description = %v", ed["description"])
	}
	if ed["url"] != "https://postgrest.org/en/v14/references/api.html" {
		t.Errorf("externalDocs url = %v", ed["url"])
	}

	want := []string{
		"application/json",
		"application/vnd.pgrst.object+json;nulls=stripped",
		"application/vnd.pgrst.object+json",
		"text/csv",
	}
	for _, key := range []string{"consumes", "produces"} {
		got := doc[key].([]any)
		if len(got) != len(want) {
			t.Fatalf("%s = %v", key, got)
		}
		for i := range want {
			if got[i] != want[i] {
				t.Errorf("%s[%d] = %v, want %v", key, i, got[i], want[i])
			}
		}
	}

	root, ok := doc["paths"].(map[string]any)["/"].(map[string]any)
	if !ok {
		t.Fatal(`missing the "/" path entry`)
	}
	get := root["get"].(map[string]any)
	if get["summary"] != "OpenAPI description (this document)" {
		t.Errorf("root summary = %v", get["summary"])
	}
	tags := get["tags"].([]any)
	if len(tags) != 1 || tags[0] != "Introspection" {
		t.Errorf("root tags = %v", tags)
	}
	produces := get["produces"].([]any)
	if len(produces) != 2 || produces[0] != "application/openapi+json" || produces[1] != "application/json" {
		t.Errorf("root produces = %v", produces)
	}
}

// TestReservedParametersMatchV14 pins the shared parameter definitions against
// the live v14 document: names, locations, descriptions, enums, and the
// explicit required:false.
func TestReservedParametersMatchV14(t *testing.T) {
	doc := decode(t, filmsModel(), nil, sqliteCaps(), Options{})
	params := doc["parameters"].(map[string]any)

	cases := map[string]map[string]any{
		"select":       {"name": "select", "in": "query", "description": "Filtering Columns"},
		"on_conflict":  {"name": "on_conflict", "in": "query", "description": "On Conflict"},
		"order":        {"name": "order", "in": "query", "description": "Ordering"},
		"range":        {"name": "Range", "in": "header", "description": "Limiting and Pagination"},
		"rangeUnit":    {"name": "Range-Unit", "in": "header", "description": "Limiting and Pagination", "default": "items"},
		"offset":       {"name": "offset", "in": "query", "description": "Limiting and Pagination"},
		"limit":        {"name": "limit", "in": "query", "description": "Limiting and Pagination"},
		"preferParams": {"name": "Prefer", "in": "header", "description": "Preference"},
		"preferReturn": {"name": "Prefer", "in": "header", "description": "Preference",
			"enum": []any{"return=representation", "return=minimal", "return=none"}},
		"preferCount": {"name": "Prefer", "in": "header", "description": "Preference",
			"enum": []any{"count=none"}},
		"preferPost": {"name": "Prefer", "in": "header", "description": "Preference",
			"enum": []any{"return=representation", "return=minimal", "return=none", "resolution=ignore-duplicates", "resolution=merge-duplicates"}},
	}
	for key, want := range cases {
		got, ok := params[key].(map[string]any)
		if !ok {
			t.Errorf("missing parameter %q", key)
			continue
		}
		if got["type"] != "string" {
			t.Errorf("%s type = %v, want string", key, got["type"])
		}
		if req, present := got["required"]; !present || req != false {
			t.Errorf("%s required = %v, want explicit false", key, req)
		}
		for f, v := range want {
			if f == "enum" {
				gotEnum := got["enum"].([]any)
				wantEnum := v.([]any)
				if len(gotEnum) != len(wantEnum) {
					t.Errorf("%s enum = %v, want %v", key, gotEnum, wantEnum)
					continue
				}
				for i := range wantEnum {
					if gotEnum[i] != wantEnum[i] {
						t.Errorf("%s enum[%d] = %v, want %v", key, i, gotEnum[i], wantEnum[i])
					}
				}
				continue
			}
			if got[f] != v {
				t.Errorf("%s %s = %v, want %v", key, f, got[f], v)
			}
		}
	}
}

// TestRelationOperationLayout pins each table operation's parameter reference
// sequence and responses against the v14 layout: GET is rowFilters then the
// read block with a 200-array-schema and a 206; POST is body/select/preferPost
// with a 201; PATCH is rowFilters/body/preferReturn and DELETE
// rowFilters/preferReturn, both 204.
func TestRelationOperationLayout(t *testing.T) {
	doc := decode(t, filmsModel(), nil, sqliteCaps(), Options{})
	films := doc["paths"].(map[string]any)["/films"].(map[string]any)

	filterRefs := []string{
		"#/parameters/rowFilter.films.id",
		"#/parameters/rowFilter.films.title",
		"#/parameters/rowFilter.films.year",
		"#/parameters/rowFilter.films.director_id",
	}
	refSeq := func(op string) []string {
		params := films[op].(map[string]any)["parameters"].([]any)
		out := make([]string, len(params))
		for i, p := range params {
			out[i], _ = p.(map[string]any)["$ref"].(string)
		}
		return out
	}
	assertSeq := func(op string, want []string) {
		t.Helper()
		got := refSeq(op)
		if strings.Join(got, " ") != strings.Join(want, " ") {
			t.Errorf("%s parameters = %v, want %v", op, got, want)
		}
	}

	assertSeq("get", append(append([]string{}, filterRefs...),
		"#/parameters/select", "#/parameters/order", "#/parameters/range", "#/parameters/rangeUnit",
		"#/parameters/offset", "#/parameters/limit", "#/parameters/preferCount"))
	assertSeq("post", []string{"#/parameters/body.films", "#/parameters/select", "#/parameters/preferPost"})
	assertSeq("patch", append(append([]string{}, filterRefs...),
		"#/parameters/body.films", "#/parameters/preferReturn"))
	assertSeq("delete", append(append([]string{}, filterRefs...), "#/parameters/preferReturn"))

	get := films["get"].(map[string]any)
	resp := get["responses"].(map[string]any)
	ok200 := resp["200"].(map[string]any)
	schema200 := ok200["schema"].(map[string]any)
	if schema200["type"] != "array" {
		t.Errorf("200 schema type = %v, want array", schema200["type"])
	}
	if schema200["items"].(map[string]any)["$ref"] != "#/definitions/films" {
		t.Errorf("200 schema items = %v", schema200["items"])
	}
	if resp["206"].(map[string]any)["description"] != "Partial Content" {
		t.Errorf("206 = %v", resp["206"])
	}
	for op, want := range map[string]string{"post": "201", "patch": "204", "delete": "204"} {
		r := films[op].(map[string]any)["responses"].(map[string]any)
		if len(r) != 1 {
			t.Errorf("%s responses = %v, want only %s", op, r, want)
		}
		if _, ok := r[want]; !ok {
			t.Errorf("%s responses lack %s", op, want)
		}
	}

	// The shared body parameter names the table and references its definition.
	body := doc["parameters"].(map[string]any)["body.films"].(map[string]any)
	if body["name"] != "films" || body["in"] != "body" || body["description"] != "films" {
		t.Errorf("body.films = %v", body)
	}
	if body["schema"].(map[string]any)["$ref"] != "#/definitions/films" {
		t.Errorf("body.films schema = %v", body["schema"])
	}
}

