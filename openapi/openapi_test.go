func TestRPCPaths(t *testing.T) {
	reg := rpc.NewStaticRegistry([]*rpc.Function{
		{
			Name:       "add",
			Params:     []rpc.Param{{Name: "a", Type: "int4"}, {Name: "b", Type: "int4"}},
			Returns:    rpc.ReturnShape{Kind: rpc.ReturnScalar, Type: "int4"},
			Volatility: rpc.Immutable,
		},
		{
			Name:       "log_event",
			Params:     []rpc.Param{{Name: "msg", Type: "text"}},
			Returns:    rpc.ReturnShape{Kind: rpc.ReturnScalar, Type: "void"},
			Volatility: rpc.Volatile,
		},
	})
	doc := decode(t, filmsModel(), reg, sqliteCaps(), Options{})
	paths := doc["paths"].(map[string]any)

	add, ok := paths["/rpc/add"].(map[string]any)
	if !ok {
		t.Fatal("missing /rpc/add")
	}
	if _, ok := add["get"]; !ok {
		t.Error("immutable function should be callable by GET")
	}
	post, ok := add["post"].(map[string]any)
	if !ok {
		t.Fatal("function should be callable by POST")
	}

	// v14 tags rpc operations "(rpc) <name>" and drops text/csv from produces.
	tags := post["tags"].([]any)
	if len(tags) != 1 || tags[0] != "(rpc) add" {
		t.Errorf("rpc tags = %v, want [(rpc) add]", tags)
	}
	produces := post["produces"].([]any)
	for _, mt := range produces {
		if mt == "text/csv" {
			t.Error("rpc produces should not list text/csv")
		}
	}
	if len(produces) != 3 || produces[0] != "application/json" {
		t.Errorf("rpc produces = %v", produces)
	}

	// POST takes the args as one required body plus the preferParams ref; GET
	// inlines one query parameter per argument.
	postParams := post["parameters"].([]any)
	if len(postParams) != 2 {
		t.Fatalf("rpc post parameters = %v", postParams)
	}
	args := postParams[0].(map[string]any)
	if args["name"] != "args" || args["in"] != "body" || args["required"] != true {
		t.Errorf("rpc args parameter = %v", args)
	}
	if postParams[1].(map[string]any)["$ref"] != "#/parameters/preferParams" {
		t.Errorf("rpc post parameters[1] = %v", postParams[1])
	}
	getParams := add["get"].(map[string]any)["parameters"].([]any)
	if len(getParams) != 2 {
		t.Fatalf("rpc get parameters = %v", getParams)
	}
	for i, name := range []string{"a", "b"} {
		p := getParams[i].(map[string]any)
		if p["name"] != name || p["in"] != "query" || p["required"] != true {
			t.Errorf("rpc get parameter %d = %v", i, p)
		}
	}

	logEvent, ok := paths["/rpc/log_event"].(map[string]any)
	if !ok {
		t.Fatal("missing /rpc/log_event")
	}
	if _, ok := logEvent["get"]; ok {
		t.Error("volatile function should not be callable by GET")
	}
	if _, ok := logEvent["post"]; !ok {
		t.Error("volatile function should be callable by POST")
	}
}

